----- Criação das tabelas -----
-------------------------------

create table continente (
       id int primary key,
       nome varchar(30)
       )

create table pais (
	sigla char(3),
	nome nvarchar(30) not null,
	continente_id int references continente(id)
	constraint pk_pais primary key(sigla),
	unique (nome))

create table circuito (
        id int primary key,
        nome varchar(100),
        cidade varchar(50),
        pais_id char(3) references pais(sigla),
        urbano bit
        )

create table gp (
       ano int,
       num int,
       nome varchar(200),
       circuito_id int references circuito(id),
       meios_pontos bit,
       unique (ano, nome),
       primary key (ano, num)
       )

create table pontuacao (
       ano_desde int,
       ano_ate int,
       posicao int,
       pontos int,
       primary key(ano_desde, posicao),
       unique(ano_ate, posicao),
       check (ano_ate >= ano_desde)
       )

create table motor
	(id int identity (1,1),
	 nome nvarchar(20)
	constraint pk_motor primary key(id))

create table piloto
	(id int identity (1,1),
	pnome nvarchar(30) ,
	unome nvarchar(30) not null,
	pais char(3),
	sexo char(1),
	anoNasc smallint,
	constraint pk_piloto primary key(id),
	constraint fk_pais_piloto foreign key(pais) references pais)

create table resultados (
       ano int,
       gp_num int,
       piloto_id int references piloto(id),
       posicao int null,
       resultado varchar(6) null -- normalmente, é só um número, mas pode ser ainda DNF, DNS, DNQ, DSQ ou outros
       primary key (gp_num, ano, piloto_id),
       foreign key (ano, gp_num) references gp(ano, num)
       )


create table epoca
	(
	ano int,
	piloto_id int,
	vitorias int,
	partidas int,
	pontos float,
	constraint pk_epoca primary key(ano, piloto_id),
        constraint fk_epoca_piloto foreign key (piloto_id) references piloto)

create table construtor
	(id int	identity(1,1),
	 nome nvarchar(20),
	pais char(3),
	anoFund smallint,
	constraint pk_const PRIMARY KEY (id),
	constraint fk_pais_construtor foreign key(pais) references pais(sigla),
	UNIQUE (nome, pais))

create table equipa (
	motor_id int,
	construtor_id int,
	piloto1_id int,
	piloto2_id int,
	ano smallint,
	constraint pk_equipa primary key (construtor_id, ano),
	unique (construtor_id, ano, piloto1_id),
	unique (construtor_id, ano, piloto2_id),
	constraint fk_eq_constr foreign key (construtor_id) references construtor,
	constraint fk_eq_p1 foreign key (piloto1_id) references piloto,
	constraint fk_eq_p2 foreign key (piloto2_id) references piloto,
	constraint fk_eq_motor foreign key (motor_id) references motor)

